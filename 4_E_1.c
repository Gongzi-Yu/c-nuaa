// 求出并输出所有的 “水仙花数”
/*
做一个三重循环，外层循环变量a表示百位数，
其合法的取值范围1至9；中间层循环变量b表示十位数，
其取值范围0至9；内层循环变量c表示个位数，其取值范
围0至9；在循环体中将a、b、c三个位数组合成一个三位
数i，判断i是否满足 “水仙花数” 的条件，若满足，则输
出i的值。
*/
#include <stdio.h>

int main()
{
	int a, b, c, i;
	for (a = 1; a < 10; a++)
	{
		for (b = 0; b < 10; b++)
		{
			for (c = 0; c < 10; c++)
			{
				i = a * 100 + b * 10 + c;
				if (i == a * a * a + b * b * b + c * c * c)
					printf("%d\t", i);
			}
		}
	}
}